package ma.canevaz.azote.model;

/**
 * Pruning intensity levels affecting nitrogen exportation through pruning wood.
 */
public enum IntensiteTaille {
    /**
     * Heavy pruning (Taille forte)
     */
    FORTE,
    
    /**
     * Medium pruning (Taille moyenne)
     */
    MOYENNE,
    
    /**
     * Light pruning (Taille légère)
     */
    LEGERE
}
